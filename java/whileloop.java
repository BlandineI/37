// This is a simple Java program to demonstrate a while loop
public class whileloop {  // craetes a public class whileloop
public static void main(String[] args) {  //declares the main method
    int i=1;  //Declare a variable i and initialize it to 1
    while(i<=12){  //Start a while loop that will iterate while i is less than or equal to 12
        System.out.println(i);  //Print the value of i to the console
    i++;  //Increment i by 1
    }  // ends the while loop
}  // ends the main method
}  //ends the public class