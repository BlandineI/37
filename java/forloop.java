public class forloop {  //declares the public class for loop
    public static void main(String[] args) {  //declares the main method
        //Code of Java for loop  
        for(int i=1;i<=15;i++){  // declares the for loop to iterate from 1 to 15
            System.out.println(i);  //prints the number i
        }  //ends the for loop
    }  //ends the main method
    } // ends the public class
