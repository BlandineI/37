public class switchc {  //creates a public class switchc
public static void main(String[] args) {  // This is the main method of the program. 
      
    int number=20;  //Declaring a variable for switch expression
 
    switch(number){  //Switch expression 
    //Case statements  
    case 10: System.out.println("10");  
    break;  
    case 20: System.out.println("20");  
    break;  
    case 30: System.out.println("30");  
    break;  
    //Default case statement  
    default:System.out.println("Not in 10, 20 or 30");  
    }  // ends the switch
}  // ends the main method
}  // ends the public class